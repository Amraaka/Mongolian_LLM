# ====================================================================
# GENERATOR
# ====================================================================

def expand(items, templates):
    return [t.format(x=item) for item in items for t in templates]


def main():
    parser = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    parser.add_argument("--limit", type=int, default=1000,
                        help="Max questions to keep after dedupe + shuffle (default 1000)")
    parser.add_argument("--seed", type=int, default=42,
                        help="Random seed for shuffling (default 42)")
    args = parser.parse_args()

    sources = [
        ("mn_people", PEOPLE_MN, PERSON_TEMPLATES),
        ("world_people", PEOPLE_WORLD, PERSON_TEMPLATES),
        ("mn_places", PLACES_MN, PLACE_TEMPLATES),
        ("world_places", PLACES_WORLD, PLACE_TEMPLATES),
        ("mn_culture", CULTURE_MN, CULTURE_TEMPLATES),
        ("science", SCIENCE, SCIENCE_TEMPLATES),
        ("events", EVENTS, EVENT_TEMPLATES),
    ]

    all_questions = []
    print("Per-category counts:")
    for name, items, templates in sources:
        qs = expand(items, templates)
        all_questions += qs
        print(f"  {name:14s}: {len(items):3d} items × {len(templates)} templates = {len(qs):4d}")
    print(f"Raw total: {len(all_questions)}")

    seen = set()
    unique = []
    for q in all_questions:
        if q not in seen:
            seen.add(q)
            unique.append(q)
    print(f"After dedupe: {len(unique)}")

    random.seed(args.seed)
    random.shuffle(unique)

    final = unique[:args.limit]
    print(f"After --limit {args.limit}: {len(final)}")

    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT_PATH.open("w", encoding="utf-8") as f:
        for q in final:
            f.write(json.dumps({"question": q}, ensure_ascii=False) + "\n")
    print(f"\nSaved {len(final)} questions to {OUTPUT_PATH}")

    print("\nFirst 8 examples:")
    for q in final[:8]:
        print(f"  - {q}")


if __name__ == "__main__":
    main()
